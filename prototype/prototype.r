distance = function(sent1, sent2) {
	#lower case
	sent1 = tolower(sent1)
	sent2 = tolower(sent2)
	
	#remove punctuation
	sent1 = gsub("[[:punct:]]", "", as.character(sent1))
	sent2 = gsub("[[:punct:]]", "", as.character(sent2))

	#split into sets
	sent1 = strsplit(sent1, " ")[[1]]
	sent2 = strsplit(sent2, " ")[[1]]
	both = intersect(sent1, sent2)
	
	return(length(both) / (log(length(sent1)) + log(length(sent2))))
}

adj_matrix = function(sentence_list) {
	n = length(sentence_list)
	adj = matrix(NA, nrow = n, ncol = n)
	
	for(i in 1:n) {
		for(j in 1:n) {
			adj[i,j] = distance(sentence_list[i], sentence_list[j])
		}
	}
	return(adj)
}

